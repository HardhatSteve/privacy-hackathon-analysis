// Token icon component that renders the appropriate icon
export type TokenType = "SOL" | "USDC" | "USDT" | "ANY";

interface TokenIconComponentProps extends TokenIconProps {
  token: TokenType;
}

export function TokenIcon({ token, ...props }: TokenIconComponentProps) {
  switch (token) {
    case "SOL":
      return <SolanaIcon {...props} />;
    case "USDC":
      return <UsdcIcon {...props} />;
    case "USDT":
      return <UsdtIcon {...props} />;
    case "ANY":
      return <AnyTokenIcon {...props} />;
    default:
      return <AnyTokenIcon {...props} />;
  }
}
